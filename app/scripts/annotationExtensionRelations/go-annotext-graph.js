    const url = "https://www.ebi.ac.uk/QuickGO/services/ontology/ae/relations";

    class GoAnnotextGraph extends LitElement {
      constructor(...args) {
        super(...args);
        this.width = 1500;
        this.height = 900;
        this.rx = 38;
        this.ry = 38;
        this.data = undefined;
        this.colorScale = Accent;
        this.simulation = undefined;
        this.link = void 0;
        this.node = void 0;
        this.text = void 0;
        this.svg = void 0;
        this.tooltip = void 0;

        this.ticked = () => {
          this.link.attr("x1", d => d.source.x).attr("y1", d => d.source.y).attr("x2", d => d.target.x).attr("y2", d => d.target.y).attr("stroke", this.colorScale[0]).attr("stroke-width", 1);
          this.node.selectAll("ellipse").attr("cx", d => d.x).attr("cy", d => d.y);
          this.text.attr("x", d => d.x).attr("y", d => d.y);
          /* We might need this if still problems with fitting inside the svg box
              const radiusX = this.rx;
              const radiusY = this.ry;
              this.node
                .attr("cx", d => Math.max(radiusX, Math.min(this.width - radiusX, d.x)))
                .attr("cy", d => Math.max(radiusY, Math.min(this.height - radiusY, d.y)));
              this.text
                .attr("x", d => Math.max(radiusX, Math.min(this.width - radiusX, d.x)))
                .attr("y", d => Math.max(radiusY, Math.min(this.height - radiusY, d.y)));
          */

          this.wrap();
        };

        this.initForceDisplay = () => {
          if (this.shadowRoot === null || typeof this.data === "undefined") {
            return;
          }

          const dataWithSizes = this.data.nodes.map(d => {
            return { ...d,
              rx: this.rx,
              ry: this.ry
            };
          });
          this.simulation = simulation().force("link", link().id(d => d.id)).force("charge", ellipseForce(6, 0.5, 5.8)).force("center", center(this.width / 2, this.height / 2));
          this.svg = select(this.shadowRoot.getElementById("annotext-graph"));
          this.tooltip = select(this.shadowRoot.getElementById("gag-tooltip"));
          this.svg.append("svg:defs").selectAll("marker").data(["end"]) // Different link/path types can be defined here
          .enter().append("svg:marker") // This section adds in the arrows
          .attr("id", String).attr("viewBox", "0 -10 15 15").attr("refX", 70).attr("refY", -1.5).attr("markerWidth", 10).attr("markerHeight", 10).attr("orient", "auto").append("svg:path").attr("d", "M0,-6L11,0L0,6"); // link lines

          this.link = this.svg.append("g").selectAll("line").data(this.data.edges).enter().append("line").attr("marker-end", "url(#end)"); // node groups

          this.node = this.svg.append("g").selectAll(".nodeGroup").data(dataWithSizes).enter().append("g").attr("class", "nodeGroup").on("click", d => {
            this.showTooltip(d, event.pageX, event.pageY);
          });
          this.node.append("ellipse").attr("rx", d => d.rx).attr("ry", d => d.ry).attr("stroke", this.colorScale[4]).attr("fill", this.colorScale[1]).attr("id", d => "node_" + d.id); // node text

          this.text = this.node.append("text").attr("dy", 2).attr("text-anchor", "middle").text(d => d.id.replace(/_/gi, " ").trim()).attr("id", d => d.id).attr("fill", "black").style("pointer-events", "none");
          this.simulation.nodes(dataWithSizes).on("tick", this.ticked);
          this.simulation.force("link").links(this.data.edges);
        };
      }

      static get properties() {
        return {
          data: {}
        };
      }

      async connectedCallback() {
        super.connectedCallback();
        const response = await fetch(url);
        this.data = await response.json();
      }

      updated() {
        if (typeof this.data === "undefined") {
          return;
        }

        this.initForceDisplay();
      }

      wrap() {
        const width = 80;
        this.svg.selectAll("tspan").remove();

        this.svg.selectAll("text")._groups[0].forEach(elem => {
          let textNode = this.svg.select("#" + elem.id),
              words = elem.id.replace(/_/gi, " ").trim().split(/\s+/).reverse(),
              word,
              line = [],
              lineHeight = 1.1,
              //em
          x = textNode.datum().x,
              lineNumber = 1,
              tspan = textNode.text(null).append("tspan"),
              firstTspan = textNode.select("tspan");

          while (word = words.pop()) {
            line.push(word);
            tspan.text(line.join(" "));

            if (tspan.node().getComputedTextLength() > width) {
              line.pop();
              tspan.text(line.join(" "));
              line = [word];
              tspan = textNode.append("tspan").attr("x", x).attr("dx", "0em").attr("dy", lineHeight + "em").text(word);
              lineNumber++;
            }
          }

          firstTspan.attr("dy", -0.3 * lineNumber + "em");
        });
      }

      showTooltip(datum, pageX, pageY) {
        this.tooltip.attr("class", "gag-closed-tooltip").html("");
        this.svg.selectAll("ellipse").attr("stroke-width", 1);
        const aNode = this.svg.select("#node_" + datum.id);
        aNode.attr("stroke-width", 5);
        this.tooltip.attr("class", "gag-opened-tooltip").style("left", pageX + "px").style("top", pageY - 28 + "px");
        this.tooltip.append("span").attr("class", "gag-tooltip-close").text("X").on("click", () => {
          aNode.attr("stroke-width", 1);
          this.tooltip.attr("class", "gag-closed-tooltip").html("");
        });
        let subsets = "";
        datum.subsets.forEach(set => {
          subsets += "<br>" + set;
        });
        this.tooltip.append("span").html(() => datum.id + "<hr>" + "<bold>GOC documentation:</bold> <a href='https://github.com/geneontology/annotation_extensions/blob/master/doc/" + datum.id + ".md' target='_blank'>" + datum.id + "</a> " + "<br>" + "<bold>GO Annotation Domain: </bold>" + datum.domain + "<br>" + "<bold>GO Annotation Range: </bold>" + datum.range + "<br>" + "<bold>Usage: </bold>" + datum.usage + "<br>" + "<bold>Subsets: </bold>" + subsets);
      }

      render() {
        return html`
      <style>
        :host {
          font-family: Helvetica, Arial, sans-serif;
          font-size: 10px;
        }
        .gag-opened-tooltip {
          visibility: visible;
          float: left;
          padding: 4px 10px 4px 4px;
          margin: 2px;
          width: 300px;
          border: 1px solid black;
          position: absolute;
          background-color: whitesmoke;
        }
        .gag-closed-tooltip {
          visibility: hidden;
          position: absolute;
        }
        .gag-tooltip-close {
          color: #fff;
          background-color: #333333;
          position: absolute;
          top: -10px;
          right: -10px;
          cursor: pointer;
          border-radius: 20px;
          width: 20px;
          height: 20px;
          font-size: 14px !important;
          text-align: center;
          border: 1px solid #fff;
        }
      </style>
      <div id="gag-tooltip" class="gag-closed-tooltip"></div>
      <svg
        id="annotext-graph"
        width="${this.width}"
        height="${this.height}"
      ></svg>
    `;
      }

    }

